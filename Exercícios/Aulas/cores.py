# Vamos agora trabalhar com cores no seu terminal

# Devemos usar inicialmente o código ANSI - Escape Sequence

# Para trabalhar com cores, devemos usar esta sintaxe: \033[style, texto, backgroundm

# style, texto e back acima são substituidos por números, onde o 0 quer dizer o padrão

print('\033[4;30;45mOlá Mundo!')